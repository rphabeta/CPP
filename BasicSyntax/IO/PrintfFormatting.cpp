#include <bits/stdc++.h>
using namespace std; 
int a = 1;
char s = 'a';
string str = "지금까지 이런 치킨은 없었다"; 
double b = 1.223123;

int main(){
    printf("극한직업 : %d\n",a); 
    printf("극한직업 : %c\n",s); 
    printf("극한직업 : %s\n",str.c_str()); 
    printf("극한직업 : %lf\n",b);
    return 0;
}
/*
극한직업 : 1 
극한직업 : a 
극한직업 : 지금까지 이런 치킨은 없었다 
극한직업 : 1.223123 
*/
